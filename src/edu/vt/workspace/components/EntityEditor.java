public boolean isValidEntity(){
    return entityValueField.getText().length() > 0 && typeList.getSelectedIndex() > 0
            && (typeList.getSelectedIndex() < typeList.getItemCount() - 1 || (entityTypeText.getText().length() > 0 &&! entityTypeText.getText().equals("<new type>"))) ;
    //return _entity.getValue().length() > 0 && _entity.getType().length() > 0;
}

