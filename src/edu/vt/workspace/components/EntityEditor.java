    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        _entity.setValue(entityValueField.getText());
        if (typeList.getSelectedIndex() < typeList.getItemCount() - 1) {
            _entity.setType((String) typeList.getSelectedItem());
        } else if (typeList.getSelectedIndex() == typeList.getItemCount() - 1 && !entityTypeText.getText().equals("<new type>")) {
            _entity.setType(entityTypeText.getText());
        }
        setVisible(false);
    }//GEN-LAST:event_saveButtonActionPerformed

