    private void loadTypes() {
        Set<String> types = EntityManager.getInstance().getEntityTypes();
        typeList.addItem("Please chose a type for this entity");
        for (String type : types) {
            typeList.addItem(type);
        }

        typeList.addItem("Define new type");
        typeList.setRenderer(new EntityTypeRenderer());

        if (types.size() > 0)
            typeList.setSelectedIndex(0);

    }

